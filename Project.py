# formats price to Indian style (Lakhs/Crores)
def format_indian_currency(number):
    n = f"{number:.2f}"
    integer_part, decimal_part = n.split('.')

    last_three = integer_part[-3:]
    remaining = integer_part[:-3]

    if remaining:
        formatted_remaining = ",".join([remaining[max(i - 2, 0):i] for i in range(len(remaining), 0, -2)][::-1])
        result = f"{formatted_remaining},{last_three}"
    else:
        result = last_three

    return f"â‚¹ {result}.{decimal_part}"


# classifies brands into tiers for better model context
def get_brand_tier(brand):
    brand = brand.upper()
    luxury_brands = ['BMW', 'MERCEDES-BENZ', 'AUDI', 'JAGUAR', 'LAND ROVER', 'VOLVO', 'PORSCHE', 'LEXUS', 'FERRARI']
    mid_range = ['TOYOTA', 'HONDA', 'VOLKSWAGEN', 'FORD', 'MAHINDRA', 'SKODA', 'JEEP', 'MG', 'KIA']

    if brand in luxury_brands:
        return 3  # Luxury
    elif brand in mid_range:
        return 2  # Mid-Range
    else:
        return 1  # Budget


# mappings for categorical features (needed for both training and prediction)
fuel_map = {'CNG': 0, 'Diesel': 1, 'Electric': 2, 'LPG': 3, 'Petrol': 4}
seller_map = {'Dealer': 0, 'Individual': 1, 'Trustmark Dealer': 2}
trans_map = {'Automatic': 0, 'Manual': 1}

filename = 'car_model.pkl'

# check if a saved model exists to skip training
if os.path.exists(filename):
    print("Saved model found! Loading...")
    with open(filename, 'rb') as f:
        saved_data = pickle.load(f)
        best_model = saved_data['model']
        le_brand = saved_data['le_brand']
        # Load feature cols if available, otherwise default to old list (backward compatibility)
        saved_features = saved_data.get('feature_cols', None)
    print("Model loaded successfully.")

else:
    print("No saved model found. Initiating training process...")

    # load raw data
    cars_data = pd.read_csv('cardekho_dataset.csv')

    # data cleaning
    cars_data.drop(columns=['Unnamed: 0'], inplace=True, errors='ignore')

    if 'brand' in cars_data.columns:
        cars_data.drop(columns=['brand'], inplace=True)

    cars_data.dropna(inplace=True)
    cars_data.drop_duplicates(inplace=True)

    # filter out cars with unrealistic mileage
    cars_data = cars_data[cars_data['km_driven'] < 500000]


    # feature engineering - extract brand name
    def get_brand_name(car_name):
        return car_name.split(' ')[0].strip()


    cars_data['car_name'] = cars_data['car_name'].apply(get_brand_name)

    # cleanup brand names
    cars_data['car_name'] = cars_data['car_name'].replace('Land', 'Land Rover')
    cars_data['car_name'] = cars_data['car_name'].replace('Mini', 'Mini Cooper')
    cars_data['car_name'] = cars_data['car_name'].replace('Isuzu', 'ISUZU')
    cars_data['car_name'] = cars_data['car_name'].str.upper()

    # feature engineering - apply brand tier
    cars_data['brand_tier'] = cars_data['car_name'].apply(get_brand_tier)

    # encoding
    le_brand = LabelEncoder()
    cars_data['car_name'] = le_brand.fit_transform(cars_data['car_name'])

    # map categories to numbers
    cars_data['fuel_type'] = cars_data['fuel_type'].map(fuel_map)
    cars_data['seller_type'] = cars_data['seller_type'].map(seller_map)
    cars_data['transmission_type'] = cars_data['transmission_type'].map(trans_map)

    # drop 'model' col (high cardinality causing overfitting)
    if 'model' in cars_data.columns:
        cars_data.drop(columns=['model'], inplace=True)

    # force column order for consistency - Added 'brand_tier'
    feature_cols = ['car_name', 'brand_tier', 'vehicle_age', 'km_driven', 'seller_type', 'fuel_type',
                    'transmission_type', 'mileage', 'engine', 'max_power', 'seats']

    # generate correlation heatmap for reporting
    print("Generating feature correlation heatmap...")
    plt.figure(figsize=(12, 8))
    sns.heatmap(cars_data[feature_cols + ['selling_price']].corr(), annot=True, cmap='coolwarm', fmt=".2f")
    plt.title("Feature Correlation Matrix")
    plt.tight_layout()
    plt.savefig('correlation_heatmap.png')
    plt.close()
    print("Heatmap saved as 'correlation_heatmap.png'.")

    x = cars_data[feature_cols]
    y = cars_data['selling_price']

    x_train, x_test, y_train, y_test = train_test_split(x, y, test_size=0.2, random_state=42)

    # define models to test
    models = {
        "Linear Regression": LinearRegression(),
        "Random Forest": RandomForestRegressor(n_estimators=100, random_state=42),
        "Gradient Boosting": GradientBoostingRegressor(n_estimators=500, random_state=42)
    }

    print("Starting training process...")
    print("-" * 50)

    model_names = []
    model_scores = []

    for name, model in models.items():
        model.fit(x_train, y_train)
        predictions = model.predict(x_test)
        score = r2_score(y_test, predictions)

        print(f"{name} Accuracy: {score * 100:.2f}%")

        model_names.append(name)
        model_scores.append(score * 100)

    print("-" * 50)

    # select best performing model
    best_model_index = model_scores.index(max(model_scores))
    best_model_name = model_names[best_model_index]
    best_model = models[best_model_name]

    print(f"Best Model Selected: {best_model_name}")

    # save model, encoder, and feature structure for future use
    print("Saving model artifact...")
    with open(filename, 'wb') as f:
        pickle.dump({
            'model': best_model,
            'le_brand': le_brand,
            'feature_cols': feature_cols
        }, f)
    print("Model saved!")

